# Exercise 3: Celsius to Fahrenheit Conversion
# 1.	Create a function named celsius_to_fahrenheit that takes a temperature in
# Celsius as a parameter and returns its equivalent temperature in
# Fahrenheit using the formula: Fahrenheit = (Celsius * 9/5) + 32.
# 2.	Demonstrate the usage of this function by converting a temperature of 25 degrees Celsius to Fahrenheit.

def celsius_to_fahrenheit(celsius):
    print((celsius * 9/5) + 32)

celsius_to_fahrenheit(25)


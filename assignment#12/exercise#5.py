# 1.	Create a function named max_of_two that takes two numbers as
#  parameters and returns the larger of the two.
# 2.	Demonstrate the usage of this function by finding the maximum of 32 and 78.

def  max_of_two(num1, num2):

    if num1 >= num2:
        print(num1)
    else:
        print(num2)
  
